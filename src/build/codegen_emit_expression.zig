pub fn emit_user_func(allocator: std.mem.Allocator, func: FuncDecl, ctx: CodegenContext, out: *std.ArrayList(u8)) !void {
    var func_ctx = ctx;
    func_ctx.type_bindings = func.type_bindings;
    func_ctx.callback_bindings = func.callback_bindings;
    var signature_owned_types = std.ArrayList([]const u8).empty;
    defer {
        for (signature_owned_types.items) |owned| allocator.free(owned);
        signature_owned_types.deinit(allocator);
    }

    const tokens = func.tokens;
    try append_fmt(allocator, out, "  (func ${s}", .{func.name});
    for (func.params) |param| {
        if (param.callback != null) continue;
        const abi_ty = func_param_abi_type(param);
        if (try resolve_union_layout_for_type_name(allocator, tokens, abi_ty, func_ctx, &signature_owned_types)) |layout| {
            defer free_union_layout(allocator, layout);
            for (layout.payload_tys, 0..) |payload_ty, idx| {
                try append_fmt(allocator, out, " (param ${s}.__union_payload_{d} {s})", .{
                    param.name,
                    idx,
                    codegen_wasm_type(func_ctx, payload_ty),
                });
            }
            try append_fmt(allocator, out, " (param ${s}.__union_tag i32)", .{param.name});
            continue;
        }
        if (find_struct_decl(func_ctx.structs, abi_ty)) |decl| {
            if (find_struct_layout(func_ctx.struct_layouts, abi_ty) == null) {
                try append_unmanaged_struct_param_fields(allocator, tokens, out, param.name, decl, abi_ty, func_ctx, &signature_owned_types);
                continue;
            }
        }
        if (is_tuple_type_name(abi_ty)) {
            try append_tuple_param_abi(allocator, out, param.name, abi_ty, func_ctx);
            continue;
        }
        try append_fmt(allocator, out, " (param ${s} {s})", .{ param.name, codegen_wasm_type(func_ctx, abi_ty) });
    }
    if (func.results.len != 0) {
        try out.appendSlice(allocator, " (result");
        for (func.results) |result| {
            try append_fmt(allocator, out, " {s}", .{codegen_wasm_type(func_ctx, result)});
        }
        try out.appendSlice(allocator, ")");
    }
    try out.appendSlice(allocator, "\n");

    var locals = LocalSet{};
    defer locals.deinit(allocator);
    try append_func_param_locals(allocator, func, func_ctx, &locals);
    try collect_body_locals(allocator, tokens, func.body_start, func.body_end, func_ctx, &locals);
    var cleanup_locals = LocalSet{};
    defer cleanup_locals.deinit(allocator);
    try append_func_param_locals(allocator, func, func_ctx, &cleanup_locals);
    try collect_direct_body_locals(allocator, tokens, func.body_start, func.body_end, func_ctx, &cleanup_locals);

    const self_tail_tco = try build_self_tail_tco(allocator, func, tokens, &locals, &cleanup_locals, func_ctx);
    defer if (self_tail_tco) |tco| allocator.free(tco.loop_label);

    for (locals.locals.items) |local| {
        if (!local.emit_decl) continue;
        try wat_function_body.emit_local_decl(allocator, out, local.name, codegen_wasm_type(func_ctx, local.ty));
    }
    if (self_tail_tco) |tco| {
        for (tco.func.params) |param| {
            if (param.callback != null) continue;
            try append_fmt(allocator, out, "    (local $__tail_arg_{s} {s})\n", .{
                param.name,
                codegen_wasm_type(func_ctx, param.ty),
            });
        }
    }
    if (func.arrow) {
        if (func.results.len != 1) return error.NoMatchingCall;
        if (!try emit_expr(allocator, tokens, func.body_start, func.body_end, &locals, func_ctx, func.results[0], out)) {
            return error.NoMatchingCall;
        }
        try emit_fallthrough_release_managed_locals(allocator, &cleanup_locals, func_ctx, out);
        try out.appendSlice(allocator, "    return\n");
    } else {
        const root_defer = DeferContext{
            .parent = null,
            .start_idx = func.body_start,
            .end_idx = func.body_end,
            .registered_end_idx = func.body_end,
        };
        const can_reach_end = body_can_reach_end(tokens, func.body_start, func.body_end);
        if (self_tail_tco) |tco| {
            try append_fmt(allocator, out, "    loop ${s}\n", .{tco.loop_label});
            try emit_self_tail_loop_local_reset(allocator, tco.func, &locals, func_ctx, out);
            try codegen_callbacks.emit_body(allocator, tokens, func.body_start, func.body_end, func.body_start, &locals, &cleanup_locals, &EMPTY_LOCAL_SET, func_ctx, func.results, func.result_items, func.result_struct, func.result_union, null, &root_defer, null, &tco, out);
            try out.appendSlice(allocator, "    end\n");
            if (func.results.len != 0 and !can_reach_end) {
                try out.appendSlice(allocator, "    unreachable\n");
            }
        } else {
            try codegen_callbacks.emit_body(allocator, tokens, func.body_start, func.body_end, func.body_start, &locals, &cleanup_locals, &EMPTY_LOCAL_SET, func_ctx, func.results, func.result_items, func.result_struct, func.result_union, null, &root_defer, null, null, out);
        }
        if (!body_ends_with_plain_return(tokens, func.body_start, func.body_end)) {
            try emit_fallthrough_release_managed_locals(allocator, &cleanup_locals, func_ctx, out);
            if (func.results.len != 0 and !can_reach_end) {
                try out.appendSlice(allocator, "    unreachable\n");
            }
        }
    }
    try wat_function_body.emit_func_close(allocator, out);
}

pub fn build_self_tail_tco(allocator: std.mem.Allocator, func: FuncDecl, tokens: []const lexer.Token, locals: *const LocalSet, cleanup_locals: *const LocalSet, ctx: CodegenContext) !?SelfTailTco {
    if (func.arrow) return null;
    if (func.results.len != 1) return null;
    if (!is_codegen_scalar_type(ctx, func.results[0])) return null;
    if (!func_has_self_tail_return(tokens, func.body_start, func.body_end, func)) return null;
    if (func_has_defer_stmt(tokens, func.body_start, func.body_end)) return null;
    for (func.params) |param| {
        if (param.callback != null) return null;
        if (param.variadic) return null;
        if (!is_codegen_scalar_type(ctx, param.ty)) return null;
    }
    for (locals.locals.items) |local| {
        if (!local.emit_decl) continue;
        if (!is_codegen_scalar_type(ctx, local.ty)) return null;
    }
    for (cleanup_locals.locals.items) |local| {
        if (!local.emit_decl) continue;
        if (!is_codegen_scalar_type(ctx, local.ty)) return null;
    }
    if (has_managed_cleanup_locals(cleanup_locals, ctx)) return null;
    return .{
        .func = func,
        .loop_label = try std.fmt.allocPrint(allocator, "__tail_{s}", .{func.name}),
    };
}

pub fn func_has_self_tail_return(tokens: []const lexer.Token, start_idx: usize, end_idx: usize, func: FuncDecl) bool {
    var i = start_idx;
    while (i < end_idx) {
        if (tok_eq(tokens[i], "return")) {
            const stmt_end = find_stmt_end(tokens, i, end_idx);
            const range = trim_parens(tokens, i + 1, stmt_end);
            const call_head = expr_call_head(tokens, range) orelse {
                i += 1;
                continue;
            };
            if (!call_head.is_intrinsic and same_callable_source_name(func.source_name, public_decl_name(tokens[call_head.name_idx].lexeme))) {
                return true;
            }
        }
        i += 1;
    }
    return false;
}

pub fn func_has_defer_stmt(tokens: []const lexer.Token, start_idx: usize, end_idx: usize) bool {
    var i = start_idx;
    while (i < end_idx) : (i += 1) {
        if (tok_eq(tokens[i], "defer")) return true;
    }
    return false;
}

pub fn has_managed_cleanup_locals(locals: *const LocalSet, ctx: CodegenContext) bool {
    for (locals.locals.items) |local| {
        if (!local.release_on_scope_exit) continue;
        if (managed_local_kind_for_type(local.ty, ctx) != null) return true;
    }
    return false;
}

pub fn emit_expr(allocator: std.mem.Allocator, tokens: []const lexer.Token, start_idx: usize, end_idx: usize, locals: *const LocalSet, ctx: CodegenContext, expected_ty: ?[]const u8, out: *std.ArrayList(u8)) CodegenError!bool {
    return emit_expr_with_move_context(allocator, tokens, start_idx, end_idx, locals, ctx, expected_ty, null, out);
}

fn append_unmanaged_struct_param_fields(
    allocator: std.mem.Allocator,
    tokens: []const lexer.Token,
    out: *std.ArrayList(u8),
    param_name: []const u8,
    decl: StructDecl,
    abi_ty: []const u8,
    func_ctx: CodegenContext,
    signature_owned_types: *std.ArrayList([]const u8),
) !void {
    for (decl.fields) |field| {
        const field_ty = try substitute_struct_field_type(allocator, decl, abi_ty, field.ty, signature_owned_types);
        try append_struct_field_abi_params(allocator, tokens, out, param_name, field.name, field_ty, func_ctx, signature_owned_types);
    }
}

fn emit_ident_literal_or_local(
    allocator: std.mem.Allocator,
    tokens: []const lexer.Token,
    tok: lexer.Token,
    expected_ty: ?[]const u8,
    locals: *const LocalSet,
    ctx: CodegenContext,
    out: *std.ArrayList(u8),
) !bool {
    if (expected_ty) |ty| {
        if (error_enum_branch_value(tokens, ty, tok.lexeme)) |value| {
            try append_fmt(allocator, out, "    i32.const {d}\n", .{value});
            return true;
        }
        if (value_enum_branch_value(ctx, tokens, ty, tok.lexeme)) |value| {
            try emit_number_const(allocator, ctx, out, value, ty);
            return true;
        }
    }
    if (std.mem.eql(u8, tok.lexeme, "true")) {
        if (expected_ty) |ty| {
            if (!std.mem.eql(u8, ty, "bool")) return false;
        }
        try out.appendSlice(allocator, "    i32.const 1\n");
        return true;
    }
    if (std.mem.eql(u8, tok.lexeme, "false")) {
        if (expected_ty) |ty| {
            if (!std.mem.eql(u8, ty, "bool")) return false;
        }
        try out.appendSlice(allocator, "    i32.const 0\n");
        return true;
    }
    if (expected_ty) |ty| {
        if (find_struct_local(locals.struct_locals.items, tok.lexeme)) |struct_local| {
            const same_ty = std.mem.eql(u8, struct_local.ty, ty);
            const no_layout = find_struct_layout(ctx.struct_layouts, ty) == null;
            if (same_ty and no_layout) {
                const decl = find_struct_decl(ctx.structs, ty) orelse return false;
                try emit_struct_fields_from_local(allocator, tokens, struct_local, decl, locals, ctx, false, out);
                return true;
            }
        }
        if (try emit_union_local_payload_for_type(allocator, tok.lexeme, ty, locals, ctx, out)) return true;
    }
    if (find_local_name(locals.locals.items, tok.lexeme)) |local_name| {
        try append_fmt(allocator, out, "    local.get ${s}\n", .{local_name});
        return true;
    }
    if (find_callback_call_arg(ctx.callback_call_args, tok.lexeme)) |callback_arg| {
        return try emit_expr(
            allocator,
            callback_arg.expr_tokens,
            callback_arg.expr_start,
            callback_arg.expr_end,
            locals,
            ctx,
            callback_arg.ty,
            out,
        );
    }
    if (local_scalar_const(tokens, tok.lexeme)) |local_const| {
        const ty = expected_ty orelse local_const.ty;
        try emit_number_const(allocator, ctx, out, local_const.value, ty);
        return true;
    }
    return false;
}

fn emit_expr_with_expected_ty(
    allocator: std.mem.Allocator,
    tokens: []const lexer.Token,
    range: Range,
    locals: *const LocalSet,
    ctx: CodegenContext,
    expected_ty: ?[]const u8,
    out: *std.ArrayList(u8),
) CodegenError!bool {
    const ty = expected_ty orelse return false;
    if (try emit_tuple_expr(allocator, tokens, range.start, range.end, locals, ctx, ty, out)) return true;
    if (try emit_expected_storage_agg_literal(allocator, tokens, range, locals, ctx, ty, out)) return true;
    if (try emit_struct_literal_expr(allocator, tokens, range.start, range.end, locals, ctx, ty, out)) return true;
    return false;
}

fn emit_expected_storage_agg_literal(
    allocator: std.mem.Allocator,
    tokens: []const lexer.Token,
    range: Range,
    locals: *const LocalSet,
    ctx: CodegenContext,
    ty: []const u8,
    out: *std.ArrayList(u8),
) CodegenError!bool {
    const elem_ty = managed_payload_elem_type_from_name(ty) orelse return false;
    if (!is_storage_agg_literal_expr(tokens, range.start, range.end)) return false;
    if (!try emit_storage_agg_literal(allocator, tokens, range.start, range.end, STORAGE_OVERWRITE_TMP_LOCAL, elem_ty, locals, ctx, out)) {
        return error.NoMatchingCall;
    }
    try append_fmt(allocator, out, "    local.get ${s}\n", .{STORAGE_OVERWRITE_TMP_LOCAL});
    return true;
}

/// Emit call arguments left-to-right; for n-ary fold ops, apply `op` after the 2nd+ arg.
/// When `require_arity` is non-null, require exactly that many args and always append one `op`.
pub fn emit_expr_with_move_context(allocator: std.mem.Allocator, tokens: []const lexer.Token, start_idx: usize, end_idx: usize, locals: *const LocalSet, ctx: CodegenContext, expected_ty: ?[]const u8, move_ctx: ?*const CallLastUseMoveContext, out: *std.ArrayList(u8)) CodegenError!bool {
    const range = trim_parens(tokens, start_idx, end_idx);
    if (range.start >= range.end) return false;

    if (try emit_expr_with_expected_ty(
        allocator,
        tokens,
        range,
        locals,
        ctx,
        expected_ty,
        out,
    )) return true;

    if (range.end == range.start + 1) {
        const tok = tokens[range.start];
        if (tok.kind == .string) {
            const ty = expected_ty orelse return false;
            if (!std.mem.eql(u8, ty, "text") and storage_elem_type_from_name(ty) == null) return false;
            const elem_ty = managed_payload_elem_type_from_name(ty) orelse return false;
            if (!std.mem.eql(u8, elem_ty, "u8")) return false;
            try emit_storage_u8_string_literal_value(allocator, tokens, range.start, ctx, out);
            return true;
        }
        if (tok.kind == .number) {
            try emit_number_const(allocator, ctx, out, tok.lexeme, expected_ty orelse "i32");
            return true;
        }
        if (tok_eq(tok, "nil")) {
            const ty = expected_ty orelse return false;
            if (!is_error_like_type(tokens, ty)) return false;
            try out.appendSlice(allocator, "    i32.const 0\n");
            return true;
        }
        if (tok.kind == .ident) {
            if (try emit_ident_literal_or_local(
                allocator,
                tokens,
                tok,
                expected_ty,
                locals,
                ctx,
                out,
            )) return true;
        }
        if (tok.kind == .ident) {
            const imported_const = imported_scalar_const(ctx, tokens, tok.lexeme) orelse return false;
            const ty = expected_ty orelse imported_const.ty;
            try emit_number_const(allocator, ctx, out, imported_const.value, ty);
            return true;
        }
        return false;
    }

    const call_head = expr_call_head(tokens, range) orelse return false;
    const call_name = tokens[call_head.name_idx].lexeme;

    if (call_head.is_intrinsic) {
        return try emit_intrinsic_call(
            allocator,
            tokens,
            call_head,
            call_name,
            locals,
            ctx,
            expected_ty,
            move_ctx,
            out,
        );
    }

    if (is_core_wasm_call_name(call_name)) return false;

    if (find_callback_binding(ctx.callback_bindings, call_name)) |binding| {
        return try emit_callback_binding_call(allocator, tokens, call_head, locals, ctx, binding, out);
    }

    if (find_func_decl_for_call_head(tokens, call_head, locals, ctx)) |func| {
        return try emit_user_func_call(allocator, tokens, call_head.args_start, call_head.args_end, locals, ctx, func, out);
    }

    if (find_wasi_host_import_for_tokens(ctx, tokens, call_name)) |wasi_import| {
        return try emit_wasi_host_import_expr(allocator, tokens, call_head.args_start, call_head.args_end, locals, ctx, wasi_import, false, out, codegen_callbacks.emit_expr);
    }

    const host_import = find_host_import_for_tokens(ctx.host_imports, tokens, call_name) orelse return false;
    var arg_start = call_head.args_start;
    var param_idx: usize = 0;
    while (arg_start < call_head.args_end) {
        const arg_end = find_arg_end(tokens, arg_start, call_head.args_end);
        if (string_literal_arg_lexeme(tokens, arg_start, arg_end)) |lexeme| {
            if (!host_param_is_ptr_len(host_import, param_idx)) return error.NoMatchingCall;
            const data = ctx.string_data.find(lexeme) orelse return error.NoMatchingCall;
            try append_fmt(allocator, out, "    i32.const {d}\n", .{data.ptr});
            try append_fmt(allocator, out, "    i32.const {d}\n", .{data.bytes.len});
            param_idx += 2;
        } else if (try emit_storage_ptr_len_host_arg(allocator, tokens, arg_start, arg_end, locals, host_import, param_idx, out)) {
            param_idx += 2;
        } else {
            const param_ty = if (param_idx < host_import.params.len) host_import.params[param_idx] else null;
            if (!try emit_expr(allocator, tokens, arg_start, arg_end, locals, ctx, param_ty, out)) {
                return false;
            }
            param_idx += 1;
        }
        arg_start = arg_end;
        if (arg_start < call_head.args_end and tok_eq(tokens[arg_start], ",")) arg_start += 1;
    }
    if (param_idx != host_import.params.len) return error.NoMatchingCall;
    try append_fmt(allocator, out, "    call ${s}\n", .{host_import.alias});
    return true;
}
