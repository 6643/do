pub fn emit_start_func(allocator: std.mem.Allocator, tokens: []const lexer.Token, ctx: CodegenContext, out: *std.ArrayList(u8)) !void {
    const start_idx = find_start_func(tokens) orelse return;
    const open_params = start_idx + 1;
    const close_params = try find_matching(tokens, open_params, "(", ")");
    const open_body = find_token(tokens, close_params + 1, tokens.len, "{") orelse return;
    const close_body = try find_matching(tokens, open_body, "{", "}");

    var locals = LocalSet{};
    defer locals.deinit(allocator);
    try collect_body_locals(allocator, tokens, open_body + 1, close_body, ctx, &locals);
    var cleanup_locals = LocalSet{};
    defer cleanup_locals.deinit(allocator);
    try collect_direct_body_locals(allocator, tokens, open_body + 1, close_body, ctx, &cleanup_locals);

    try wat_function_body.emit_func_open(allocator, out, "_start");
    for (locals.locals.items) |local| {
        if (!local.emit_decl) continue;
        try wat_function_body.emit_local_decl(allocator, out, local.name, codegen_wasm_type(ctx, local.ty));
    }
    const no_results: []const []const u8 = &.{};
    const root_defer = DeferContext{
        .parent = null,
        .start_idx = open_body + 1,
        .end_idx = close_body,
        .registered_end_idx = close_body,
    };
    var backend_ir_body = std.ArrayList(u8).empty;
    defer backend_ir_body.deinit(allocator);
    const emitted_backend_ir = try emit_scalar_numeric_start_with_backend_ir(allocator, tokens, open_body + 1, close_body, &locals, ctx, &backend_ir_body);
    if (emitted_backend_ir) {
        try out.appendSlice(allocator, "    ;; backend-ir-lowering scalar-numeric-start\n");
        try out.appendSlice(allocator, backend_ir_body.items);
    }
    if (!emitted_backend_ir) {
        try codegen_callbacks.emit_body(allocator, tokens, open_body + 1, close_body, open_body + 1, &locals, &cleanup_locals, &EMPTY_LOCAL_SET, ctx, no_results, NO_RESULT_ITEMS, null, null, null, &root_defer, null, null, out);
    }
    if (!body_ends_with_plain_return(tokens, open_body + 1, close_body)) {
        try emit_fallthrough_release_managed_locals(allocator, &cleanup_locals, ctx, out);
    }
    try wat_function_body.emit_func_close(allocator, out);
    try wat_function_body.emit_func_export(allocator, out, "_start", "_start");
}

const BackendIrLocal = struct {
    name: []const u8,
    value: codegen_ir.ValueId,
};

pub fn emit_scalar_numeric_start_with_backend_ir(allocator: std.mem.Allocator, tokens: []const lexer.Token, start_idx: usize, end_idx: usize, locals: *const LocalSet, ctx: CodegenContext, out: *std.ArrayList(u8)) CodegenError!bool {
    var func = try codegen_ir.Function.create(allocator, "_start_ir");
    defer func.deinit(allocator);
    const block_id = try func.add_block_id(allocator);

    var ir_locals = std.ArrayList(BackendIrLocal).empty;
    defer ir_locals.deinit(allocator);

    var i = start_idx;
    var saw_return = false;
    while (i < end_idx) {
        const stmt_end = find_stmt_end(tokens, i, end_idx);
        if (is_plain_nil_return_stmt(tokens, i, stmt_end)) {
            try func.set_terminator(block_id, .ret);
            saw_return = true;
            i = stmt_end;
            continue;
        }

        const scalar_ty = typed_scalar_binding_type(tokens, i, stmt_end, ctx) orelse return false;
        if (!std.mem.eql(u8, scalar_ty, "i32")) return false;
        const eq_idx = find_top_level_token(tokens, i + 1, stmt_end, "=") orelse return false;
        const target_source_name = tokens[i].lexeme;
        const target_name = resolved_local_name(locals.locals.items, target_source_name);
        const value = func.alloc_value();
        try func.set_value_name(allocator, value, target_name);
        try ir_locals.append(allocator, .{ .name = target_source_name, .value = value });
        try ir_locals.append(allocator, .{ .name = target_name, .value = value });

        if (!try append_scalar_numeric_expr_ir(allocator, tokens, eq_idx + 1, stmt_end, "i32", &func, block_id, ir_locals.items)) {
            return false;
        }
        try func.append_instr(allocator, block_id, .{ .local_set = value });
        i = stmt_end;
    }
    if (!saw_return) return false;

    const body = try codegen_ir.emit_function_body_wat(allocator, &func);
    defer allocator.free(body);
    try out.appendSlice(allocator, body);
    return true;
}

pub fn is_plain_nil_return_stmt(tokens: []const lexer.Token, start_idx: usize, end_idx: usize) bool {
    return start_idx + 1 == end_idx and tok_eq(tokens[start_idx], "return");
}

pub fn append_scalar_numeric_expr_ir(allocator: std.mem.Allocator, tokens: []const lexer.Token, start_idx: usize, end_idx: usize, expected_ty: []const u8, func: *codegen_ir.Function, block_id: codegen_ir.BlockId, ir_locals: []const BackendIrLocal) CodegenError!bool {
    if (!std.mem.eql(u8, expected_ty, "i32")) return false;
    const range = trim_parens(tokens, start_idx, end_idx);
    if (range.start >= range.end) return false;

    if (range.start + 1 == range.end) {
        const tok = tokens[range.start];
        if (tok.kind == .number) {
            const value = std.fmt.parseInt(i32, tok.lexeme, 0) catch return false;
            try func.append_instr(allocator, block_id, .{ .const_value = .{ .i32 = value } });
            return true;
        }
        if (tok.kind == .ident) {
            const local = find_backend_ir_local(ir_locals, tok.lexeme) orelse return false;
            try func.append_instr(allocator, block_id, .{ .local_get = local });
            return true;
        }
        return false;
    }

    const call_head = expr_call_head(tokens, range) orelse return false;
    if (!call_head.is_intrinsic) return false;
    const op = numeric_core_ir_op(tokens[call_head.name_idx].lexeme) orelse return false;

    var arg_start = call_head.args_start;
    var emitted = false;
    while (arg_start < call_head.args_end) {
        const arg_end = find_arg_end(tokens, arg_start, call_head.args_end);
        if (!try append_scalar_numeric_expr_ir(allocator, tokens, arg_start, arg_end, expected_ty, func, block_id, ir_locals)) {
            return false;
        }
        if (emitted) {
            try func.append_instr(allocator, block_id, .{ .numeric = .{ .ty = .i32, .op = op } });
        }
        emitted = true;
        arg_start = arg_end;
        if (arg_start < call_head.args_end and tok_eq(tokens[arg_start], ",")) arg_start += 1;
    }
    return emitted;
}

pub fn find_backend_ir_local(ir_locals: []const BackendIrLocal, name: []const u8) ?codegen_ir.ValueId {
    for (ir_locals) |local| {
        if (std.mem.eql(u8, local.name, name)) return local.value;
    }
    return null;
}

pub fn numeric_core_ir_op(name: []const u8) ?codegen_ir.NumericOp {
    if (std.mem.eql(u8, name, "add")) return .add;
    if (std.mem.eql(u8, name, "sub")) return .sub;
    if (std.mem.eql(u8, name, "mul")) return .mul;
    return null;
}

