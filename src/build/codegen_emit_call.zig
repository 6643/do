pub fn append_tuple_param_abi(allocator: std.mem.Allocator, out: *std.ArrayList(u8), base: []const u8, tuple_ty: []const u8, ctx: CodegenContext) CodegenError!void {
    const arity = tuple_arity(tuple_ty) orelse return error.UnsupportedLowering;
    var idx: usize = 0;
    while (idx < arity) : (idx += 1) {
        const elem_ty = tuple_element_type_at(tuple_ty, idx) orelse return error.UnsupportedLowering;
        const nested_base = try std.fmt.allocPrint(allocator, "{s}.{d}", .{ base, idx });
        defer allocator.free(nested_base);
        if (is_tuple_type_name(elem_ty)) {
            try append_tuple_param_abi(allocator, out, nested_base, elem_ty, ctx);
        } else {
            try append_fmt(allocator, out, " (param ${s} {s})", .{
                nested_base,
                codegen_wasm_type(ctx, elem_ty),
            });
        }
    }
}
