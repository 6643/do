pub fn direct_managed_call_last_use_move_source(
    tokens: []const lexer.Token,
    start_idx: usize,
    end_idx: usize,
    move_ctx: CallLastUseMoveContext,
    locals: *const LocalSet,
    ctx: CodegenContext,
) ?LastUseManagedMoveSource {
    if (end_idx != start_idx + 1 or tokens[start_idx].kind != .ident) return null;
    const source_name = tokens[start_idx].lexeme;
    const actual_name = direct_managed_local_expr_name(tokens, start_idx, end_idx, locals, ctx) orelse return null;
    const origin = find_local_origin(locals.locals.items, source_name) orelse .unknown;
    const after_arg_use = token_range_uses_ident(tokens, end_idx, move_ctx.stmt_end, source_name);
    const after_stmt_use = token_range_uses_ident(tokens, move_ctx.stmt_end, move_ctx.body_end, source_name);
    const candidate = ownership_facts.MoveCandidate{
        .kind = .call_arg,
        .source = .{
            .source_name = source_name,
            .actual_name = actual_name,
            .origin = facts_source_origin(origin),
        },
        .expr_range = .{ .start = start_idx, .end = end_idx },
        .context = .{
            .body = .{ .start = move_ctx.body_start, .end = move_ctx.body_end },
            .statement = .{ .end = move_ctx.stmt_end },
            .arg = .{ .start = start_idx, .end = end_idx },
            .defer_visible = has_registered_defer_stmt(tokens, move_ctx.defer_ctx),
            .allow_last_use_move = move_ctx.allow_last_use_move,
        },
        .future_use = .{
            .after_arg = if (after_arg_use) .{ .start = end_idx, .end = move_ctx.stmt_end } else null,
            .after_stmt = if (after_stmt_use) .{ .start = move_ctx.stmt_end, .end = move_ctx.body_end } else null,
        },
    };
    const decision = ownership_facts.decide_call_arg_move(candidate);
    if (!decision.accepted) return null;
    return .{
        .source_name = source_name,
        .actual_name = actual_name,
        .origin = origin,
    };
}

pub fn direct_managed_union_binding_call_move_source(
    tokens: []const lexer.Token,
    start_idx: usize,
    end_idx: usize,
    args_end: usize,
    stmt_end: usize,
    body_end: usize,
    allow_last_use_move: bool,
    locals: *const LocalSet,
    ctx: CodegenContext,
    defer_ctx: ?*const DeferContext,
) ?LastUseManagedMoveSource {
    if (end_idx != start_idx + 1 or tokens[start_idx].kind != .ident) return null;
    if (!allow_last_use_move) return null;
    if (has_registered_defer_stmt(tokens, defer_ctx)) return null;
    const source_name = tokens[start_idx].lexeme;
    const actual_name = direct_managed_local_expr_name(tokens, start_idx, end_idx, locals, ctx) orelse return null;
    const origin = find_local_origin(locals.locals.items, source_name) orelse .unknown;
    if (token_range_uses_ident(tokens, end_idx, args_end, source_name)) return null;
    if (token_range_uses_ident(tokens, stmt_end, body_end, source_name)) return null;
    return .{
        .source_name = source_name,
        .actual_name = actual_name,
        .origin = origin,
    };
}

